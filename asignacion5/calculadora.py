import math
from math import factorial

def suma(num1, num2):
    print("El resultado de la suma es: ", (num1+num2))

def resta(num1, num2):
    print("El resultado de la resta es: ", (num1-num2))

def multiplicacion(num1, num2):
    print("El resultado de la multiplicación es: ", (num1*num2))

def division(num1, num2):
    print("El resultado de la división es: ", (num1/num2))

def modulo(num1, num2):
    print("El resultado del módulo es: ", (num1%num2))

def potencia(num1, num2):
    print("El resultado de la potencia es: ", (num1**num2))

def raiz(num1):
    print("El resultado de la raiz es: ", (math.sqrt(num1)))

def fact(num1):
    print("El factorial es: ", (factorial(num1)))